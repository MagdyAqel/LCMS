export function getStageCatalog(stageName: unknown) {
  const name = String(stageName ?? "");

  if (name.includes("??????")) {
    return curriculumCatalog.filter((entry) => entry.stage === "basicLower");
  }

  if (name.includes("??????")) {
    return curriculumCatalog.filter((entry) => entry.stage === "basicUpper");
  }

  if (name.includes("????????")) {
    return curriculumCatalog.filter((entry) => entry.stage === "secondary");
  }

  return curriculumCatalog;
}

export function requiresTrack(gradeId: unknown) {
  const grade = String(gradeId ?? "");
  return grade === "11" || grade === "12";
}

export function getCurriculumEntriesForGrade(
  gradeId: unknown,
  track?: unknown,
) {
  const gradeNumber = Number(gradeId);
  const selectedTrack = String(track ?? "");

  return curriculumCatalog.filter((entry) => {
    if (entry.gradeNumber !== gradeNumber) {
      return false;
    }

    if (!requiresTrack(gradeId)) {
      return true;
    }

    return Boolean(selectedTrack) && entry.track === selectedTrack;
  });
}

export function getSubjectsForGrade(gradeId: unknown, track?: unknown) {
  return getCurriculumEntriesForGrade(gradeId, track).flatMap(
    (entry) => entry.subjects,
  );
}
